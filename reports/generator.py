def _save_report(project_id: str, report: str) -> None:
    """Save report as Markdown file."""
    reports_dir = Path(get_settings().reports_dir)
    reports_dir.mkdir(parents=True, exist_ok=True)
    file_path = reports_dir / f"{project_id}.md"
    file_path.write_text(report, encoding="utf-8")
    logger.info("Saved MD report: %s", file_path)


def _clean_text(text: str) -> str:
    """Clean Markdown formatting for PDF output."""
    text = re.sub(r"\*\*(.*?)\*\*", r"\1", text)  # Bold
    text = re.sub(r"\*(.*?)\*", r"\1", text)  # Italic
    text = re.sub(r"`(.*?)`", r"\1", text)  # Code
    text = text.encode("latin-1", errors="replace").decode("latin-1")
    return text
