"""
StartupPilot AI — Report Generator

Generates comprehensive startup analysis reports in three formats:
- Markdown (.md) — clean, readable, version-control friendly
- HTML (.html) — styled with professional CSS, embeddable diagrams
- PDF (.pdf) — downloadable, shareable

Uses Jinja2 for HTML templating and fpdf2 for PDF generation.
"""

