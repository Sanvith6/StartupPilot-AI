def generate_report(state: dict[str, Any]) -> str:
    """Generate a Markdown report from the analysis state.

    Compiles all agent outputs into a structured, professional report.

    Args:
        state: The complete StartupAnalysisState after workflow completion.

    Returns:
        The report content as a Markdown string.
    """
    startup_idea = state.get("startup_idea", "Unknown Startup")
    project_id = state.get("project_id", "unknown")
    timestamp = time.strftime("%B %d, %Y", time.localtime())

    sections = [
        _title_section(startup_idea, timestamp),
        _executive_summary(state),
        _industry_analysis(state),
        _market_analysis(state),
        _competitor_analysis(state),
        _swot_analysis(state),
        _business_strategy(state),
        _agent_discussion(state),
        _architecture_design(state),
        _cost_analysis(state),
        _final_recommendations(state),
        _appendix(state),
    ]

    report = "\n\n---\n\n".join(sections)

    # Save report files
    _save_report(project_id, report)

    logger.info("Generated report for project %s (%d chars)", project_id, len(report))
    return report


