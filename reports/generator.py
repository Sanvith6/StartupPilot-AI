def save_report_as_pdf(project_id: str, markdown_content: str) -> str:
    """Convert Markdown report to PDF.

    Uses fpdf2 for pure-Python PDF generation (no system dependencies).
    Returns the file path of the saved PDF.
    """
    try:
        from fpdf import FPDF

        pdf = FPDF()
        pdf.set_auto_page_break(auto=True, margin=15)
        pdf.add_page()

        # Title
        pdf.set_font("Helvetica", "B", 20)
        pdf.cell(0, 15, "StartupPilot AI Report", ln=True, align="C")
        pdf.set_font("Helvetica", "", 10)
        pdf.cell(0, 8, f"Project: {project_id}", ln=True, align="C")
        pdf.ln(10)

        # Content — process Markdown line by line
        lines = markdown_content.split("\n")
        for line in lines:
            line = line.strip()

            if line.startswith("# "):
                pdf.ln(5)
                pdf.set_font("Helvetica", "B", 16)
                pdf.multi_cell(0, 8, _clean_text(line[2:]))
                pdf.ln(3)
            elif line.startswith("## "):
                pdf.ln(4)
                pdf.set_font("Helvetica", "B", 14)
                pdf.multi_cell(0, 7, _clean_text(line[3:]))
                pdf.ln(2)
            elif line.startswith("### "):
                pdf.ln(3)
                pdf.set_font("Helvetica", "B", 12)
                pdf.multi_cell(0, 6, _clean_text(line[4:]))
                pdf.ln(2)
            elif line.startswith("- ") or line.startswith("* "):
                pdf.set_font("Helvetica", "", 10)
                pdf.multi_cell(0, 5, f"  • {_clean_text(line[2:])}")
            elif line.startswith("---"):
                pdf.ln(5)
                pdf.line(10, pdf.get_y(), 200, pdf.get_y())
                pdf.ln(5)
            elif line:
                pdf.set_font("Helvetica", "", 10)
                pdf.multi_cell(0, 5, _clean_text(line))
            else:
                pdf.ln(3)

        reports_dir = Path(get_settings().reports_dir)
        reports_dir.mkdir(parents=True, exist_ok=True)
        file_path = reports_dir / f"{project_id}.pdf"
        pdf.output(str(file_path))

        logger.info("Saved PDF report: %s", file_path)
        return str(file_path)

    except Exception as e:
        logger.error("PDF generation failed: %s", e)
        return ""


