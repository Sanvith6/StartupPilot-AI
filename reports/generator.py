def _appendix(state: dict[str, Any]) -> str:
    metrics = state.get("execution_metrics", {})
    routing = state.get("llm_routing_log", [])

    appendix = "## 📎 Appendix: Analysis Metadata\n\n"
    appendix += "### Agent Execution Metrics\n\n"
    appendix += "| Agent | Time (ms) | Model Used |\n|---|---|---|\n"

    for agent, data in metrics.items():
        if isinstance(data, dict):
            time_ms = data.get("time_ms", data.get("execution_time_ms", "N/A"))
            model = data.get("model_used", data.get("model", "N/A"))
            appendix += f"| {agent} | {time_ms} | {model} |\n"

    if routing:
        appendix += "\n### LLM Routing Decisions\n\n"
        appendix += "| Task | Provider | Model |\n|---|---|---|\n"
        for r in routing:
            appendix += f"| {r.get('task', 'N/A')} | {r.get('provider', 'N/A')} | {r.get('model', 'N/A')} |\n"

    return appendix


